// "Цена вопроса" (Петя, 2026-07-10: "советы даются реактивно... а то что это вызовет
// последствия — не говорится"). ПЕРВАЯ версия показывала ВСЕ ненулевые эффекты из RULES_TABLE
// (у некоторых категорий — 10+ статов в одной фразе) — Петя сразу же: "очень перегружено по
// статам, новичку вообще непонятно всё будет". Теперь: только заметные эффекты (среднее по
// модулю диапазона ≥1.5, отсекает шум вроде ±0..1) и не больше 3 самых крупных — компактная
// фраза, не стена цифр. Один переиспользуемый форматтер на данных движка вместо ручного текста
// на каждую ветку — не может разойтись с реальными формулами, как уже произошло с
// peace_track/peace_progress (см. фикс ниже).
function describeSideEffects(category) {
  const table = RULES_TABLE[category];
  if (!table) return "";
  const notable = Object.entries(table)
    .map(([key, [lo, hi]]) => ({ key, lo, hi, avg: (Math.abs(lo) + Math.abs(hi)) / 2 }))
    .filter(e => e.avg >= 1.5)
    .sort((a, b) => b.avg - a.avg)
    .slice(0, 3)
    .map(({ key, lo, hi }) => {
      const label = STAT_LABELS_RU[key] || key;
      const fmt = (n) => (n > 0 ? `+${n}` : `${n}`);
      return lo === hi ? `${label} ${fmt(lo)}` : `${label} ${fmt(lo)}..${fmt(hi)}`;
    });
  if (notable.length === 0) return "";
  const opLimitNote = CATEGORY_GROUP.military_operations.has(category)
    ? " Плюс месячный лимит военных операций." : "";
  return ` Цена вопроса: ${notable.join(", ")}.${opLimitNote}`;
}

// Отслеживание "выполнил ли игрок совет" (Петя, 2026-07-10: "я её выполнил — а всё так же
// висит такая же подсказка") — раньше это умела только эконом-реформа (ECON_REFORM_CATEGORIES
// ниже). Обобщаем: если последнее принятое решение — та же категория, что рекомендуется снова,
// явно показываем прогресс (было→стало по ключевому стату этой ветки) вместо молчаливого повтора
// идентичного текста.
function describeFollowThrough(category, key, label, stats, statHistory, recentCategories) {
  if (!Array.isArray(recentCategories) || recentCategories[recentCategories.length - 1] !== category) return "";
  if (!Array.isArray(statHistory) || statHistory.length === 0) return "";
  const before = statHistory[statHistory.length - 1]?.stats?.[key];
  const after = stats[key];
  if (typeof before !== "number" || typeof after !== "number") return "";
  const delta = Math.round((after - before) * 10) / 10;
  return `✓ В прошлом ходу вы уже выбрали это направление: ${label} ${before}→${after} (${delta >= 0 ? "+" : ""}${delta}). `;
}

/**
 * Детерминированный расчёт оптимального хода из формул движка (не ИИ).
 * Приоритет: сначала отвести от порога поражения тот показатель, что ближе всех
 * к нему, потом чинить казну/инфляцию, потом двигаться к победе.
 * Пороги поражения: экономика <30, рейтинг <30, стабильность <25, дипломатия <15.
 * Победа военная (с хода 8): армия ≥85, мораль/готовность ≥70, стаб/рейтинг ≥52,
 * экономика ≥36, Донецк/Луганск 100 + 2 из 3 (Запорожье ≥85, Херсон ≥65, Харьков ≥50).
 * Победа дипломатическая (с хода 12): мирный трек 100 + экономика/рейтинг/стабильность ≥65.
 *
 * ВАЖНО (баг из реальной партии, ход 3/24: экономика 55→29 и defeat_collapse раньше,
 * чем margin<10 вообще успел сработать): опасность считалась ТОЛЬКО по текущему запасу
 * до порога, без учёта скорости падения. Показатель с запасом 25, но падающий на 8-9/ход,
 * рушится за 2-3 хода — раньше, чем "безопасный" margin≥10 это заметит. Поэтому опасность
 * теперь считается и по margin, и по проекции "через сколько ходов порог при текущем темпе".
 */
function computeOptimalMove(stats, turnNumber = 1, statHistory = [], recentCategories = []) {
  const s = (k, d = 50) => (typeof stats[k] === "number" ? stats[k] : d);
  const economy = s("economy"), approval = s("approval"), stability = s("stability"),
    diplomacy = s("diplomacy"), treasury = s("treasury", 52), inflation = s("inflation", 64),
    military = s("military"), peace = s("peace_progress", 0);

  // 1. Опасность: расстояние до порога поражения + скорость падения
  const dangers = [
    { key: "economy", margin: economy - 30, label: "экономика" },
    { key: "approval", margin: approval - 30, label: "рейтинг" },
    { key: "stability", margin: stability - 25, label: "стабильность" },
    { key: "diplomacy", margin: diplomacy - 15, label: "дипломатия" },
  ].map(d => {
    const velocity = computeVelocity(statHistory, d.key);
    const turnsLeft = velocity < -0.5 ? d.margin / -velocity : Infinity;
    return { ...d, velocity, turnsLeft, urgent: d.margin < 10 || turnsLeft <= 2 };
  }).sort((a, b) => {
    // Срочные — первыми, по числу ходов до обвала; дальше — по обычному запасу.
    if (a.urgent !== b.urgent) return a.urgent ? -1 : 1;
    if (a.urgent) return a.turnsLeft - b.turnsLeft;
    return a.margin - b.margin;
  });
  const worst = dangers[0];

  // Порог 10: месячная автоэрозия экономики ограничена −6, плюс цена действий —
  // запас меньше 10 пунктов уже может быть съеден за один месяц.
  // ИЛИ показатель падает так быстро, что порог будет пробит за 1-2 хода несмотря
  // на текущий запас (см. комментарий к функции выше).
  if (worst.urgent) {
    const trendNote = worst.margin >= 10
      ? ` При этом темпе (−${Math.abs(worst.velocity).toFixed(1)}/ход) порог будет пробит примерно через ${Math.max(1, Math.ceil(worst.turnsLeft))} ход(а) — раньше, чем кажется по текущему запасу ${worst.margin}. Разворачивать курс нужно СЕЙЧАС, а не когда запас кончится.`
      : "";
    if (worst.key === "economy") {
      // При пустой казне стимулирование гасится дефицитной спиралью — сначала казна.
      if (treasury < 10) {
        const category = "econ_austerity";
        return { advisorId: "finance", category, mode: "decree_fast", direction: "economic_austerity",
          title: "Бюджетная консолидация (срочно)",
          decree: "Правительству поручаю программу срочной бюджетной консолидации: сокращение некритических расходов и повышение сборов с экспортёров.",
          reason: `${describeFollowThrough(category, "economy", "Экономика", stats, statHistory, recentCategories)}Экономика ${economy} — до коллапса (<30) осталось ${worst.margin} п.${trendNote} Казна ${treasury} — стимулирование при пустой казне гасится дефицитной спиралью, сначала нужно закрыть дефицит. Никаких военных операций в этом месяце: end-month снимет ещё до −6 автоматических потерь.${describeSideEffects(category)}` };
      }
      const category = "econ_stimulus";
      return { advisorId: "finance", category, mode: "decree_fast", direction: "economic_stimulus",
        title: "Стимулирование экономики (срочно)",
        decree: "Правительству поручаю разработать пакет мер по льготному кредитованию малого и среднего бизнеса.",
        reason: `${describeFollowThrough(category, "economy", "Экономика", stats, statHistory, recentCategories)}Экономика ${economy} — до коллапса (<30) осталось ${worst.margin} п.${trendNote} Стимул даёт +1..+3, автоматическая эрозия за месяц ограничена −6, так что одного стимула может не хватить — стоит воздержаться от дорогих военных операций.${describeSideEffects(category)}` };
    }
    if (worst.key === "approval") {
      const category = "pol_social";
      return { advisorId: "press", category, mode: "decree_fast", direction: "domestic_liberalization",
        title: "Социальная программа (срочно)",
        decree: "Утверждаю социальную программу поддержки семей военнослужащих и ветеранов.",
        reason: `${describeFollowThrough(category, "approval", "Рейтинг", stats, statHistory, recentCategories)}Рейтинг ${approval} — до переворота (<30) осталось ${worst.margin} п.${trendNote} Социальные меры — самый прямой рычаг одобрения (+1..+2); мобилизация и жёсткая экономия сейчас недопустимы.${describeSideEffects(category)}` };
    }
    if (worst.key === "stability") {
      const category = "pol_propaganda";
      return { advisorId: "press", category, mode: "decree_fast", direction: "info_narrative",
        title: "Информационная кампания (срочно)",
        decree: "Поручаю госСМИ развернуть информационную кампанию об успехах на фронте и героизме военнослужащих.",
        reason: `${describeFollowThrough(category, "stability", "Стабильность", stats, statHistory, recentCategories)}Стабильность ${stability} — до волнений (<25) осталось ${worst.margin} п.${trendNote} Информационная кампания поднимает стабильность и рейтинг без удара по казне (подавление подняло бы стабильность, но обвалило бы рейтинг ${approval}).${describeSideEffects(category)}` };
    }
    const category = "diplo_negotiate";
    return { advisorId: "foreign", category, mode: "diplomacy_op", direction: "diplomacy_outreach",
      title: "Дипломатические переговоры (срочно)",
      decree: "Министерству иностранных дел поручаю провести переговоры с Турцией и ОАЭ о посредничестве в мирном процессе.",
      reason: `${describeFollowThrough(category, "diplomacy", "Дипломатия", stats, statHistory, recentCategories)}Дипломатия ${diplomacy} — до изоляции (<15) осталось ${worst.margin} п.${trendNote} Переговоры — единственный прямой рычаг; эскалация и тайные операции сейчас усугубят изоляцию.${describeSideEffects(category)}` };
  }

  // 2. Казна в минусе — спираль вниз тянет экономику каждый месяц
  if (treasury < 0) {
    return { advisorId: "finance", category: "econ_austerity", mode: "decree_fast", direction: "economic_austerity",
      title: "Закрыть дефицит казны",
      decree: "Правительству поручаю программу бюджетной консолидации с сокращением некритических расходов.",
      reason: `Казна ${treasury} — дефицит каждый месяц давит экономику (${economy}) и разгоняет инфляцию. Пока дефицит не закрыт, любые дорогие операции углубляют спираль.${describeSideEffects("econ_austerity")}` };
  }

  // 3. Инфляция выше порога — ежемесячный штраф экономике и рейтингу
  if (inflation > 73 && economy < 55) {
    return { advisorId: "finance", category: "econ_austerity", mode: "decree_reform", direction: "economic_austerity",
      title: "Сбить инфляцию",
      decree: "Правительству поручаю программу бюджетной консолидации: сдержать госрасходы и не давить на ЦБ за снижение ставки.",
      reason: `Инфляция: индекс ${Math.round(inflation)} (порог 73, ~${Math.max(0, Math.round(inflation - 58))}% г/г) — каждый месяц −1..−3 экономике и рейтингу. Консолидация снижает дефицитное давление; ставку ЦБ поднимет сам.${describeSideEffects("econ_austerity")}` };
  }

  // 4. Опасностей нет — путь к победе
  const donetsk = s("donetsk_control", 0), luhansk = s("luhansk_control", 0),
    zap = s("zaporizhzhia_control", 0), kher = s("kherson_control", 0), khar = s("kharkiv_control", 0);
  const secondary = [zap >= 85, kher >= 65, khar >= 50].filter(Boolean).length;
  const territoriesClose = donetsk >= 80 && luhansk >= 90;

  if (territoriesClose && military >= 75 && economy >= 45 && treasury >= 35) {
    const target = donetsk < 100 ? { nom: "Донецкое", prep: "Донецком", key: "donetsk_control" }
      : luhansk < 100 ? { nom: "Луганское", prep: "Луганском", key: "luhansk_control" }
      : secondary < 2 ? { nom: "Запорожское", prep: "Запорожском", key: "zaporizhzhia_control" } : null;
    if (target) {
      // Следующая цель ПОСЛЕ этой (Петя, 2026-07-10: "помоги дойти до победы" — дорожная карта,
      // не только текущий шаг). Порядок совпадает с приоритетом выбора target выше.
      const nextAfter = target.key === "donetsk_control"
        ? (luhansk < 100 ? `После Донецка — Луганск (уже ${luhansk}%).` : secondary < 2 ? `После Донецка — доп. регионы (Запорожье ${zap}%, Херсон ${kher}%, Харьков ${khar}%, нужно 2 из 3).` : "")
        : target.key === "luhansk_control"
        ? (secondary < 2 ? `После Луганска — доп. регионы (Запорожье ${zap}%, Херсон ${kher}%, Харьков ${khar}%, нужно 2 из 3).` : "")
        : "";
      const category = "mil_operational_offensive";
      return { advisorId: "defense", category, mode: "military", direction: "military_offensive",
        title: `Наступление (${target.nom} направление)`,
        decree: `Приказываю начать наступательную операцию на ${target.prep} направлении.`,
        reason: `${describeFollowThrough(category, target.key, target.nom, stats, statHistory, recentCategories)}Военная победа близко (Донецк ${donetsk}, Луганск ${luhansk}, доп. регионы ${secondary}/2). Экономика ${economy} и казна ${treasury} выдержат месячное военное бремя (потолок автопотерь −6/мес). Нужно ещё ${100 - s(target.key, 0)}% на ${target.nom.toLowerCase()} направлении. ${nextAfter}${describeSideEffects(category)}` };
    }
  }

  if (peace >= 40 || turnNumber >= 10) {
    const economyGap = Math.max(0, 65 - economy), approvalGap = Math.max(0, 65 - approval), stabilityGap = Math.max(0, 65 - stability);
    const gapsNote = (economyGap || approvalGap || stabilityGap)
      ? ` Разрывы до порога победы: экономика −${economyGap}, рейтинг −${approvalGap}, стабильность −${stabilityGap} (нужно 65 по каждому).`
      : " Экономика/рейтинг/стабильность уже держат порог 65 — остаётся только мирный трек.";
    const category = "diplo_peace";
    return { advisorId: "foreign", category, mode: "diplomacy_op", direction: "peace_initiative",
      title: "Мирная инициатива",
      decree: "Поручаю МИДу инициировать переговоры об урегулировании конфликта при посредничестве Турции и ОАЭ.",
      reason: `${describeFollowThrough(category, "peace_progress", "Мирный трек", stats, statHistory, recentCategories)}Мирный трек ${peace}/100 — самая дешёвая дипломатическая операция и главный двигатель к дипломатической победе.${gapsNote}${describeSideEffects(category)}` };
  }

  // Дефолт (экономика) — но НЕ дублировать реформу, если она уже в работе. Реальный баг из
  // живой партии: этот дефолт срабатывает каждый ход, когда острых угроз нет, — раньше он
  // ВСЕГДА советовал одну и ту же экономическую реформу, даже если игрок только что её
  // подписал. Игрок подписал 5 реформ за 3 хода подряд, ни одна не успела дать эффект (лаг
  // 1-3 хода через ВВП), экономика рухнула быстрее, чем реформы окупились. Проверяем последние
  // ходы: если экономическая реформа уже в работе — советуем ПОДОЖДАТЬ, а не дублировать.
  const ECON_REFORM_CATEGORIES = new Set(["econ_stimulus", "econ_infrastructure", "econ_tech"]);
  const REFORM_COOLDOWN_TURNS = 3; // тот же лаг, что и у компаундинга gdp_growth → economy
  if (recentCategories.slice(-REFORM_COOLDOWN_TURNS).some(c => ECON_REFORM_CATEGORIES.has(c))) {
    return { advisorId: "finance", category: "econ_hold", mode: "decree_fast", direction: "null_action",
      title: "Дать реформе сработать",
      decree: null,
      reason: `Острых угроз нет, экономическая реформа уже подписана в последних ходах — её эффект доходит до экономики с лагом 1-3 хода через рост ВВП, а не сразу. Подписывать ещё одну поверх непрошедшей — тратить инициативу и глушить сигнал: станет непонятно, что от чего сработало. Дайте текущей реформе отработать, в следующие ходы оцените результат по темпу роста ВВП.` };
  }
  return { advisorId: "finance", category: "econ_stimulus", mode: "decree_reform", direction: "economic_stimulus",
    title: "Укреплять экономику",
    decree: "Правительству поручаю разработать пакет мер по стимулированию экономики и поддержке несырьевого экспорта.",
    reason: `Острых угроз нет. Экономика ${economy} — фундамент обоих путей к победе (военный требует ≥36 на момент победы, дипломатический ≥65). Запас прочности против месячной эрозии (до −6) окупается всегда.${(() => {
      const auto = computeExpectedEconomyEffects(stats);
      return auto.total < 0
        ? ` Но в ЭТОМ ходу ждите автоматическое ${auto.total} (${auto.effects.map(e => e.label).join(", ")}) — реформа доходит до экономики только через 1-3 хода, база сработает раньше. Это не признак ошибки.`
        : "";
    })()}${describeSideEffects("econ_stimulus")}` };
}

// Динамическая часть промпта — всё, что меняется от хода к ходу (см. комментарий у
// CACHED_SYSTEM). Собирается через обычную интерполяцию шаблонных строк, а не replaceAll по
// плейсхолдерам — это заодно устраняет исходный класс бага (2026-07-07: строковый .replace брал
// только первое вхождение повторяющегося плейсхолдера, второе уходило в промпт буквально).
function buildAdvisorsUserMessage({ countryName, playerName, gameDate, turnNumber, stats, relations, policies, recentHistory, playerDraft, actionMode, optimalMove, statHistory, language, advisorId }) {
  // Свободный текст от президента именно этому советнику — может быть черновиком указа,
  // прямым вопросом или просто описанием ситуации своими словами. Формулировка инструкции
  // намеренно не предполагает конкретный жанр — советник должен ответить по существу написанного,
  // а не считать это обязательно готовым указом.
  const draftSection = playerDraft
    ? `СООБЩЕНИЕ ПРЕЗИДЕНТА ИМЕННО ВАМ (может быть черновиком указа, вопросом или описанием ситуации — отреагируйте по существу написанного):\n"${playerDraft}"`
    : `Президент пока ничего не написал вам напрямую — дайте рекомендацию исходя из текущей обстановки и выбранного типа действия.`;

  const actionModeDesc = SCALE_DESCRIPTIONS[actionMode] || SCALE_DESCRIPTIONS[actionMode?.replace("decree", "decree_reform")] || SCALE_DESCRIPTIONS.decree_reform;
  // Полный текст масштаба уже есть в CACHED_SYSTEM (раздел "МАСШТАБ ДЕЙСТВИЯ") — здесь достаточно
  // короткой метки (первая строка описания), чтобы не дублировать один и тот же текст в каждом
  // вызове дважды.
  const actionModeLabel = actionModeDesc.split("\n")[0];

  const optimalMoveText = optimalMove
    ? `Ход: ${optimalMove.title}\nФормулировка указа: ${optimalMove.decree ? `"${optimalMove.decree}"` : "(нет — совет подождать)"}\nРасчёт: ${optimalMove.reason}`
    : "(расчёт недоступен)";

  const historyText = recentHistory.length
    ? recentHistory.map(h => `Ход ${h.turn_n}: "${h.player_input}" → ${h.narrative_text}`).join("\n")
    : "(партия только началась, истории нет)";

  const advisorMeta = ADVISORS.find(a => a.id === advisorId);
  const isOptimalCarrier = !!optimalMove && optimalMove.advisorId === advisorId;

  // Раньше (когда все 5 советников отвечали одним вызовом) расчётный ход всегда доставался
  // тому советнику, чья сфера ему соответствует, а остальные явно видели, что могут
  // соглашаться/предлагать альтернативы. Теперь каждый советник отвечает отдельным вызовом —
  // если СЕЙЧАС спрашивают не носителя расчёта, он просто не знает о нём вообще (это не его
  // сфера) и даёт совет исключительно из своей роли, без ссылки на чужой расчёт.
  const optimalSection = isOptimalCarrier
    ? `═══════════════════════════════════════════
МАТЕМАТИЧЕСКИ РАССЧИТАННЫЙ ОПТИМАЛЬНЫЙ ХОД (из формул движка, не мнение)
═══════════════════════════════════════════
${optimalMoveText}

ОБЯЗАТЕЛЬНО: именно ВЫ (${advisorMeta?.name || advisorId}) должны предложить ИМЕННО этот ход —
если указана "Формулировка указа", ваш proposed_decree должен близко повторять её, а
recommendation — объяснить расчёт своими словами (в стиле персонажа). Если вместо формулировки
указа стоит "(нет — совет подождать)" — указ НЕ придумывать: recommendation объясняет, почему
сейчас лучше не подписывать новый указ в этой сфере (например, реформа уже в работе),
suggested_direction — "null_action", proposed_decree — пустая строка.
`
    : "";

  return `СЕЙЧАС СПРАШИВАЮТ МНЕНИЕ СОВЕТНИКА: [${advisorId}] ${advisorMeta?.name || ""} — ${advisorMeta?.role || ""}
Отвечайте ТОЛЬКО от лица этого советника.

Президент: ${playerName || "Господин Президент"}

ПРЕЗИДЕНТ СЕЙЧАС РАССМАТРИВАЕТ: ${actionModeLabel}
(полное описание этого масштаба — в разделе «МАСШТАБ ДЕЙСТВИЯ» выше; совет должен
соответствовать ИМЕННО этому масштабу, а не остальным перечисленным там же)

═══════════════════════════════════════════
ТЕКУЩАЯ ОБСТАНОВКА
═══════════════════════════════════════════
Страна: ${countryName}
Дата: ${gameDate}, Ход №${turnNumber}

Показатели: ${JSON.stringify(stats)}
Отношения: ${JSON.stringify(relations.slice(0, 8))}
Активные политики: ${JSON.stringify(policies)}

ЭКОНОМИЧЕСКИЕ ИНДИКАТОРЫ (Силин должен их прокомментировать, если сейчас спрашивают его):
${buildEconIndicators(stats)}

═══════════════════════════════════════════
ТЕМП ИЗМЕНЕНИЯ КЛЮЧЕВЫХ ПОКАЗАТЕЛЕЙ (пункты/ход, по факту последних ходов)
═══════════════════════════════════════════
${buildTrendSection(stats, statHistory)}
ВАЖНО: показатель с большим запасом до порога, но падающий быстро, может обрушиться
за 2-3 хода — раньше, чем "безопасный" запас это подскажет. Если ниже отмечено ⚠️ и это
ваша сфера — прямо предупредите о темпе, а не только назовите текущее значение.

${optimalSection}
ИСТОРИЯ (${recentHistory.length} ходов, от старых к новым):
${historyText}

${draftSection}` + languageInstruction(language);
}

function stripMarkdownFences(text) {
  return text.replace(/```json\s*|\s*```/g, "").trim();
}

// Расход ИИ (Петя, 2026-07-08): раньше ОДИН вызов возвращал мнения ВСЕХ пяти советников сразу,
// и фронт дёргал его автоматически при загрузке/смене режима/переключении вкладки — большая
// часть из пяти мнений часто даже не читалась. Теперь фронт показывает портреты с приветствием
// (без ИИ) и просит мнение ОДНОГО конкретного советника по явному клику — типичный игрок читает
// 1-2 мнения вместо всех пяти на каждый чих, кратное снижение числа вызовов. Заодно max_tokens
// упал с 2500 (пять мнений) до 700 (одно) — четвёртый рычаг экономии сверх кэширования.
async function consultAdvisor({ params, advisorId, callClaudeApi, meta }) {
  if (!ADVISORS.some(a => a.id === advisorId)) {
    throw new Error(`Unknown advisorId: ${advisorId}`);
  }
  const optimalMove = computeOptimalMove(params.stats || {}, params.turnNumber, params.statHistory, params.recentCategories);
  const userMessage = buildAdvisorsUserMessage({ ...params, optimalMove, advisorId });

  const response = await callClaudeApi({
    model: "claude-haiku-4-5-20251001",
    max_tokens: 700,
    system: [{ type: "text", text: CACHED_SYSTEM, cache_control: { type: "ephemeral" } }],
    messages: [{ role: "user", content: userMessage }],
  }, meta);

  const rawText = response.content
    .filter(b => b.type === "text")
    .map(b => b.text)
    .join("\n");

  let parsed;
  try {
    parsed = JSON.parse(stripMarkdownFences(rawText));
  } catch {
    return fallbackConsult(advisorId, optimalMove);
  }

  const advisorMeta = ADVISORS.find(a => a.id === advisorId) || {};
  const isOptimal = !!optimalMove && optimalMove.advisorId === advisorId;

  return {
    advisor: { id: advisorId, name: advisorMeta.name, role: advisorMeta.role, ...parsed, is_optimal: isOptimal },
    optimalMove,
  };
}

function fallbackConsult(advisorId, optimalMove) {
  // ИИ недоступен, но детерминированный расчёт работает всегда —
  // носитель оптимального хода получает его текстом вместо заглушки.
  const advisorMeta = ADVISORS.find(a => a.id === advisorId) || {};
  const isOptimal = !!optimalMove && optimalMove.advisorId === advisorId;
  return {
    advisor: {
      id: advisorId,
      name: advisorMeta.name,
      role: advisorMeta.role,
      recommendation: isOptimal
        ? `По расчёту аппарата: ${optimalMove.reason}`
        : "Запрос к советнику временно недоступен. Примите решение самостоятельно.",
      proposed_decree: isOptimal ? optimalMove.decree : undefined,
      suggested_direction: isOptimal ? optimalMove.direction : "null_action",
      suggested_scale: isOptimal ? optimalMove.mode : undefined,
      tone: "cautious",
      is_optimal: isOptimal,
    },
    optimalMove: optimalMove || null,
  };
}

module.exports = { consultAdvisor, computeOptimalMove, ADVISORS };
